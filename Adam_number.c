#include<stdio.h>
int main()
{
    int n,sum=0,d,k,temp;
    scanf("%d",&n);
    temp=n*n;
    while(n)
    {
        d=n%10;
        sum=(sum*10)+d;
        n=n/10;
    }
    k=sum*sum;
    sum=0;
    while(k)
    {
        d=k%10;
        sum=(sum*10)+d;
        k=k/10;
    }
    if(temp==sum)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}